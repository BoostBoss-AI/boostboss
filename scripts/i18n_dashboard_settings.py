LANGS = [
    ('en',    EN),
    ('zh',    ZH),
    ('zh-TW', ZH_TW),
    ('ja',    JA),
    ('ko',    KO),
    ('vi',    VI),
]


def validate_parity():
    en_keys = set(EN.keys())
    for code, d in LANGS:
        missing = en_keys - set(d.keys())
        extra   = set(d.keys()) - en_keys
        if missing:
            print(f"!! {code} is missing keys: {sorted(missing)}", file=sys.stderr)
            sys.exit(1)
        if extra:
            print(f"!! {code} has extra keys not in EN: {sorted(extra)}", file=sys.stderr)
            sys.exit(1)


def escape_js_string(s: str) -> str:
    # JS single-quoted string: escape backslashes and single quotes.
    return s.replace('\\', '\\\\').replace("'", "\\'")


def render_dash_t() -> str:
    """Return the JS source for `const DASH_T = { … };` with consistent
    12-space-indent for the opener and matching nested indentation. The
    output goes back EXACTLY where the existing PATTERN matched."""
    out = []
    out.append('            const DASH_T = {')
    for code, d in LANGS:
        out.append(f"                '{code}': {{")
        for key in EN.keys():
            val = escape_js_string(d[key])
            out.append(f"                    '{key}': '{val}',")
        out.append('                },')
    out.append('            };')
    return '\n'.join(out)


# Match the entire `const DASH_T = { ... };` block regardless of how deep
# its nested indentation got. We anchor on the opener (`const DASH_T = {`)
# and a `};` that's by itself on its own line (any indent). Greedy then
# trimmed via non-greedy on the inside.
PATTERN = re.compile(
    r" *const DASH_T = \{.*?\n[ \t]+\};",
    re.DOTALL,
)


def patch_file(path: Path):
    text = path.read_text(encoding='utf-8')
    new_block = render_dash_t()
    new_text, n = PATTERN.subn(new_block, text, count=1)
    if n != 1:
        print(f"!! Could not locate DASH_T block in {path}", file=sys.stderr)
        sys.exit(1)
    if new_text == text:
        print(f"-- {path.name}: no change")
        return
    path.write_text(new_text, encoding='utf-8')
    print(f"++ {path.name}: DASH_T regenerated ({len(EN)} keys × {len(LANGS)} languages)")


def main():
    validate_parity()
    patch_file(ROOT / 'public' / 'developer.html')
